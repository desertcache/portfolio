export const CELL = 8; // monospace advance, matches the 8px maze tile grid

// Draw text whose top-left cell corner is (x, y). align: 'left'|'right'|'center'.
export function drawText(ctx, text, x, y, { color = '#fff', scale = 1, align = 'left' } = {}) {
  const str = String(text).toUpperCase();
  const width = str.length * CELL * scale;
  let cx = x;
  if (align === 'right') cx = x - width;
  else if (align === 'center') cx = x - width / 2;

  ctx.fillStyle = color;
  for (const ch of str) {
    const rows = GLYPHS[ch];
    if (rows) {
      for (let r = 0; r < 7; r++) {
        const bits = rows[r];
        for (let c = 0; c < 5; c++) {
          if (bits & (1 << (4 - c))) {
            ctx.fillRect(cx + (c + 1) * scale, y + (r + 1) * scale, scale, scale);
          }
        }
      }
    }
    cx += CELL * scale;
  }
}
